VALOR = int(input('Insira um valor de 1 até 7: '))

if VALOR == 1:
    print('Domingo')
elif VALOR == 2:
    print('Segunda')
elif VALOR == 3:
    print('Terça')
elif VALOR == 4:
    print('Quarta')
elif VALOR == 5:
    print('Quinta')
elif VALOR == 6:
    print('Sexta')
elif VALOR == 7:
    print('Sábado')
else:
    print('Valor inválido')
